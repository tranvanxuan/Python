# tra ve mot phan tu ngau nhien tu chuoi chi dinh
# chuoi co the la mot chuoi, danh sach, tuple hoac bat ki loai nao kkac

import random
mylist=["apple", "banana", "cherry"]
# tra ve mot phan tu ngau nhien trong danh sach
print(random.choice(mylist))

# cu phap random.choice (sequence)
# sequence : chuoi, list, tuple, a rang of numbers, ...

x="WELCOME"
# tra ve ngau nhien mot ki tu trong x
print(random.choice(x))